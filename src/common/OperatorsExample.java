package common;

public class OperatorsExample {
	public static void main(String[] args) {
		int var1 = 100;
		byte var2 = 50;
		int result = (var1 + var2);
		
		System.out.println("두 수의 합은 : " + result);
		System.out.println("---------------------------------");
		
		//float, double(실수의 기본)
		float f1 = 10.1F;
		double f2 = 5.5F;
		double f3 = f1 + f2;
		System.out.println(f3);
		 
		
	}

}
